    private void jButton_crearcarroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_crearcarroMouseClicked
        //String marca, String modelo, int anio, String estado, int costo, String ID 
        DefaultComboBoxModel rt = (DefaultComboBoxModel) jComboBox2.getModel();
        carros temporalcarros = new carros(jTextField_marcacrearcarro.getText(), jTextField_modelocrearcarro.getText(),
                Integer.parseInt(jFormattedTextFieldjTextField_aniocrearcarro.getText()), "espera de entrar a reparacion",
                Integer.parseInt(jFormattedTextField_jTextField_costocrearcarro.getText()), jTextField_IDcrearcarro.getText());
        admincarros ap = new admincarros();
        ap.leerArchivo();
        ap.getListacarros().add(temporalcarros);
        ap.escribirArchivo();

        for (carros object : ap.getListacarros()) {
            rt.addElement(object.getID());
        }

        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setNumRows(0);
        for (carros object2 : ap.getListacarros()) {
            dt.addRow(new Object[]{object2.getID(), object2.getModelo(), object2.getMarca(), object2.getCosto()});
        }
        DefaultComboBoxModel cm2 = (DefaultComboBoxModel) jComboBox4.getModel();
        cm2.removeAllElements();
        for (carros object : ap.getListacarros()) {
            cm2.addElement(object.getID());
        }
        DefaultComboBoxModel cm = (DefaultComboBoxModel) jComboBox2.getModel();
        cm.removeAllElements();
        for (carros object : ap.getListacarros()) {
            cm.addElement(object.getID());
        }
    }//GEN-LAST:event_jButton_crearcarroMouseClicked

